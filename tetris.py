def draw_grid(pen, grid):
    top = 200
    left = -130
    colors = ["green", "black", "lightblue", "yellow", "red", "blue", "orange", "green", "purple"]
    for row in range(len(grid)):
        for col in range(len(grid[0])):
            screen_x = left + (col*15)
            screen_y = top - (row*15)
            if grid[row][col] == 0:
                pen.color(colors[0])
                pen.goto(screen_x, screen_y)
                pen.stamp()
draw_grid(pen, grid)



